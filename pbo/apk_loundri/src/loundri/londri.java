    private void brtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brtActionPerformed
        if(brt.getText().equals("")){
            th.setText("0");
        }else{
            int harga = Integer.parseInt(hp.getText());
            int berat = Integer.parseInt(brt.getText());
            int Total_Harga = harga * berat;
            th.setText(String.valueOf(Total_Harga));
        }
    }//GEN-LAST:event_brtActionPerformed

