    private void btn_transaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_transaksiActionPerformed
        if(hp.getText().isEmpty()|| brt.getText().isEmpty() || th.getText().isEmpty() || byr.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Harus Diisi");
        } else {
            int bayar = Integer.parseInt(byr.getText());
            int total = Integer.parseInt(th.getText());
            int kembalian = bayar - total;
            JOptionPane.showMessageDialog(null, "Kembalian anda : \n"+kembalian);
        } 
    }//GEN-LAST:event_btn_transaksiActionPerformed

    private void brtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_brtKeyReleased
        if(brt.getText().equals("")){
            th.setText("0");
        }else{
            int harga = Integer.parseInt(hp.getText());
            int berat = Integer.parseInt(brt.getText());
            int Total_Harga = harga * berat;
            th.setText(String.valueOf(Total_Harga));
        }
    }//GEN-LAST:event_brtKeyReleased

